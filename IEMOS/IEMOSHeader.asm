.686
.MMX
.XMM
.model flat,stdcall
option casemap:none

include windows.inc

include IEMOS.inc


.CODE


IEMOS_ALIGN
;------------------------------------------------------------------------------
; IEMOSHeader - Returns in eax a pointer to header or NULL if not valid
;------------------------------------------------------------------------------
IEMOSHeader PROC USES EBX hIEMOS:DWORD
    .IF hIEMOS == NULL
        mov eax, NULL
        ret
    .ENDIF
    mov ebx, hIEMOS
    mov eax, [ebx].MOSINFO.MOSHeaderPtr
    ret
IEMOSHeader ENDP



IEMOS_LIBEND

