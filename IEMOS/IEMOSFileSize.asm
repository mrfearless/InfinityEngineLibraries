.XMM
.model flat,stdcall
option casemap:none

include windows.inc

include IEMOS.inc


.CODE


IEMOS_ALIGN
;------------------------------------------------------------------------------
; IEMOSFileSize - returns in eax size of file or NULL
;------------------------------------------------------------------------------
IEMOSFileSize PROC USES EBX hIEMOS:DWORD
    .IF hIEMOS == NULL
        mov eax, NULL
        ret
    .ENDIF
    mov ebx, hIEMOS
    mov eax, [ebx].MOSINFO.MOSFilesize
    ret
IEMOSFileSize ENDP



IEMOS_LIBEND

